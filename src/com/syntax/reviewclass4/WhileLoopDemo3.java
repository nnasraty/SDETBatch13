package com.syntax.reviewclass4;

public class WhileLoopDemo3 {

	public static void main(String[] args) {
		// Print from 10 to 1
		int y=10;
		while(y>1) {
			System.out.print(y);
			y=y-1;		// or y--;
		}
		
		
		//Print Odd numbers
		System.out.println();
		int z=9; // we should change the value from 10 to 9
		while(z>=1) {
			System.out.print(z);
			z=z-2;		
		}
		
		
		
		
	}

}
